// Helper functions
export function getClientBySlug(slug: string): ClientData | undefined {
  return clients.find((c) => c.slug === slug);
}

export function getAllSlugs(): string[] {
  return clients.map((c) => c.slug);
}

export function getFeaturedClients(): ClientData[] {
  return clients.filter((c) => c.featured);
}

export function getAllCategories(): string[] {
  return ['All', ...Array.from(new Set(clients.map((c) => c.category))).sort()];
}
